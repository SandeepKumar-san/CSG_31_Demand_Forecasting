class USGSDataset(Dataset):
    """
    PyTorch Dataset for windowed time-series from USGS.

    Same interface as SupplyGraphDataset for compatibility.
    Each sample returns:
      - time_series:     [seq_length, n_features]
      - price_history:   [seq_length]
      - static_features: [n_static]
      - targets:         [n_horizons]
      - material_type:   int
      - product_id:      int
      - horizon:         int

    Args:
        feature_matrix: [n_nodes, n_timepoints, n_features]
        demand_matrix:  [n_nodes, n_timepoints]
        static_features: [n_nodes, n_static]
        graph_data: PyTorch Geometric Data object
        seq_length: lookback window size
        horizons: list of forecast horizons
        start_idx: start of time window
        end_idx: end of time window
        seed: random seed
    """

    def __init__(
        self,
        feature_matrix: np.ndarray,
        demand_matrix: np.ndarray,
        static_features: np.ndarray,
        graph_data,
        seq_length: int,
        horizons: List[int],
        valid_positions: List[int],
        seed: int = 42,
    ) -> None:
        super().__init__()
        self.feature_matrix = feature_matrix
        self.demand_matrix = demand_matrix
        self.static_features = static_features
        self.graph_data = graph_data
        self.seq_length = seq_length
        self.horizons = horizons
        self.max_horizon = max(horizons) if horizons else 1
        self.seed = seed

        n_nodes = feature_matrix.shape[0]

        # Build sample list: (commodity_id, time_idx) for all valid positions
        self.samples: List[Tuple[int, int]] = []

        for nid in range(n_nodes):
            for t in valid_positions:
                self.samples.append((nid, t))

    def __len__(self) -> int:
        return len(self.samples)

    def __getitem__(self, idx: int) -> Dict[str, torch.Tensor]:
        nid, t = self.samples[idx]

        # ---- Time series features [seq_length, n_features] ----
        time_series = self.feature_matrix[nid, t - self.seq_length : t, :]

        # ---- Price/demand history [seq_length] ----
        price_history = self.demand_matrix[nid, t - self.seq_length : t]

        # ---- Static features [n_static] ----
        static = self.static_features[nid]

        # ---- Targets at each horizon ----
        targets = np.array(
            [self.demand_matrix[nid, t + h] for h in self.horizons],
            dtype=np.float32,
        )

        # ---- Material type ----
        material_type = int(self.graph_data.material_types[nid].item())

        return {
            "time_series": torch.tensor(time_series, dtype=torch.float),
            "price_history": torch.tensor(price_history, dtype=torch.float),
            "static_features": torch.tensor(static, dtype=torch.float),
            "targets": torch.tensor(targets, dtype=torch.float),
            "material_type": torch.tensor(material_type, dtype=torch.long),
            "product_id": torch.tensor(nid, dtype=torch.long),
            "horizon": torch.tensor(idx % len(self.horizons), dtype=torch.long),
        }
