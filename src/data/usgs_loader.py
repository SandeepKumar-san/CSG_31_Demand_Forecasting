"""
USGS Mineral Commodity Data Loader.

Loads preprocessed USGS data from data/processed/usgs/ and prepares
datasets for the Adaptive Temporal-Structural Fusion model.

Expected preprocessed files (generated by usgs_preprocessing.py):
  data/processed/usgs/
  ├── NodesIndex.csv         ← node_id, commodity_name
  ├── Node Types.csv         ← node_id, commodity_name, category, category_name
  ├── all_edges.csv          ← source_id, target_id, edge_type_id, edge_weight
  ├── Production.csv         ← Year, 0, 1, 2, ..., N-1  (wide format)
  ├── Consumption.csv
  ├── Imports.csv
  ├── Exports.csv
  ├── Price.csv
  └── Stocks.csv

Usage:
    loader = USGSLoader(config)
    train_ds, val_ds, test_ds, info = loader.prepare_datasets()
"""

import os
from typing import Any, Dict, List, Optional, Tuple

import numpy as np
import pandas as pd
import torch
from torch.utils.data import Dataset
from torch_geometric.data import Data


